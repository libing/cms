				<H3>
					Zeg niet zomaar schoen tegen een (technische) loopschoen. Elk
					exemplaar is anders en opgebouwd volgens de noden van de loper. Er
					zijn schoenen voor zwaar- en lichtgewichten, voor elk voettype,
					voor elk looppatroon. <B>Veertig procent van alle loopblessures
						wordt veroorzaakt door het dragen van een fout schoentype.</B> Bij
					de aanschaf van loopschoenen moet dus met meer rekening gehouden
					worden dan enkel de kwaliteit van de schoen.
				</H3>
				<P>Hoe kiezen wij uw loopschoen?</P>
				<DIV class="cm-fl quotes">
					<DIV class="cm-fl q-no">1</DIV>
					<DIV class="cm-fl q-text">Blootvoetse loop- of wandelanalyse</DIV>
				</DIV>
				<P>Wij bieden bij elk loopschoenenadvies een footscan®analyse aan.
					Tijdens die wetenschappelijke voetanalyse stapt of loopt u
					blootvoets over een 2-meter drukplaat. Terwijl u dit doet, meten
					16384 sensoren aan een zeer hoge frequentie (500 beelden per
					seconde) het drukverloop van de voetzool. Simultaan wordt de
					beweging gefilmd. Aan de hand van die meetresultaten kunnen onze
					ervaren beweeganalisten uw voettype en looppatroon gedetailleerd
					analyseren en het correcte type loopschoenen adviseren.</P>
